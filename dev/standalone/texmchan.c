/**************************** CHECK_TEXMCHAN_PARAM_VALIDITY_AND_CONSISTENCY *****************************/

int check_texmchan_param_validity_and_consistency(dataptr dz)
{
	int exit_status, n, kk;
	double *p, *end, maxpos, maxsprd;
	int OK = TRUE;
	if(dz->brksize[TEXTURE_INSLO]) {
		p = dz->brk[TEXTURE_INSLO] + 1;
		end = dz->brk[TEXTURE_INSLO] + (dz->brksize[TEXTURE_INSLO] * 2);
		while(p < end) {
			if(*p  > (double)dz->infilecnt) {
				OK = FALSE;
				break;
			}
			p += 2;
		}
	} else if(dz->iparam[TEXTURE_INSLO] > dz->infilecnt)
		OK  = FALSE;
	if(!OK) {
		sprintf(errstr,"FIRST SND-IN-LIST TO USE > count of files entered: cannot proceed.\n");
		return(DATA_ERROR);
	}
	OK = TRUE;
	if(dz->brksize[TEXTURE_INSHI]) {
		p = dz->brk[TEXTURE_INSHI] + 1;
		end = dz->brk[TEXTURE_INSHI] + (dz->brksize[TEXTURE_INSHI] * 2);
		while(p < end) {
			if(*p  > (double)dz->infilecnt) {
				OK = FALSE;
				break;
			}
			p += 2;
		}
	} else if(dz->iparam[TEXTURE_INSHI] > dz->infilecnt)
		OK  = FALSE;
	if(!OK) {
		sprintf(errstr,"LAST SND-IN-LIST TO USE > count of files entered: cannot proceed.\n");
		return(DATA_ERROR);
	}
	if(dz->brksize[TEXTURE_POS] == 0)
		maxpos = dz->param[TEXTURE_POS];
	else {
		if((exit_status = get_maxvalue_in_brktable(&maxpos,TEXTURE_POS,dz))<0)
			return(exit_status);
	}
	if(maxpos > (double)dz->iparam[TEXTURE_OUTCHANS]) {
		sprintf(errstr,"Largest Position Value(s) Exceeds Number Of Output Channels.\n");
		return(DATA_ERROR);
	}
	if(dz->brksize[TEXTURE_SPRD] == 0)
		maxsprd = dz->param[TEXTURE_SPRD];
	else {
		if((exit_status = get_maxvalue_in_brktable(&maxsprd,TEXTURE_SPRD,dz))<0)
			return(exit_status);
	}
	if(maxsprd > (double)dz->iparam[TEXTURE_OUTCHANS]) {
		sprintf(errstr,"Largest Spread Value(s) Exceeds Number Of Output Channels.\n");
		return(DATA_ERROR);
	}
	/* set up array for perming outchannels selected */
	if((dz->iparray[6] = (int *)malloc(dz->iparam[TEXTURE_OUTCHANS] * sizeof(int)))==NULL) {
		sprintf(errstr,"Insufficient Memory For Output Position Perming Array.\n");
		return(MEMORY_ERROR);
	}
	for(n=0;n<dz->iparam[TEXTURE_OUTCHANS];n++)	/* Fill channel permuting array with initial values */
		dz->iparray[6][n] = n;

	if(dz->vflag[4] == 1) {
		if(dz->iparam[TEXTURE_OUTCHANS] <= 2) {
			sprintf(errstr,"FIXED POSITION FLAG ONLY AVAILABLE WHEN OUTPUT CHANNEL COUNT IS > 2\n");
			return(DATA_ERROR);
		}
		kk = dz->infilecnt/dz->iparam[TEXTURE_OUTCHANS];
		if((kk * dz->iparam[TEXTURE_OUTCHANS]) != dz->infilecnt)
			kk++;
		kk *= dz->iparam[TEXTURE_OUTCHANS];
		// kk is an exact multiple of TEXTURE_OUTCHANS
		if((dz->parray[1]  = (double *)malloc(kk * sizeof(double)))==NULL) {
			sprintf(errstr,"INSUFFICIENT MEMORY for storage of infile positions.\n");
			return(MEMORY_ERROR);
		}
		if((dz->iparray[dz->iarray_cnt-1]  = (int *)malloc(kk * sizeof(int)))==NULL) {
			sprintf(errstr,"INSUFFICIENT MEMORY for storage of infile positions.\n");
			return(MEMORY_ERROR);
		}
	}
	return(FINISHED);
}

